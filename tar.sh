# explanation for tar command
1.creating tar file

tar cvf name.tar directoryname

2.extracting from tar file

tar xvf name.tar

3.view tar file

tar tvf name.tar file

note : c -creating x-extracting t-view

cvf =creating verbosely following archive file

cvfz =z for gzip file with extension .gz
cvfj =j for bzip file with extension .bz2
